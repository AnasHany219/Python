# -------------------------------- Boolean --------------------------------
# [1] In programming, You need to know if your code output is True or False
# [2] Boolean values are the two constrant object -> False || True

name = " "
print(name.isspace())               # True
print(100 > 200)                    # False
print(200 > 100)                    # True

# bool()
print(bool("A"))                    # True
print(bool(219))                    # True
print(bool(2.1))                    # True
print(bool(-21))                    # True
print(bool(0))                      # False
print(bool(""))                     # False
print(bool(''))                     # False
print(bool({}))                     # False
print(bool([]))                     # False

