# ------------------- Arithmetic Operators -------------------
# Addition
print(21 + 9)           # 30
print(-21 + 9)          # -12
print(21.9 + 9.3)       # 31.2

# Subtraction
print(21 - 9)           # 21
print(-21 - 9)          # -30
print(21.9 - 9.3)       # 12.599999999999998    ~ 12.6

# Multiplication
print(21 * 9)           # 189
print(-21 * 9)          # -189
print(21.9 * 9.3)       # 203.67000000000002    ~ 203.67

# Division
print(21 / 9)           # 2.3333333333333335
print(-21 / 9)          # -2.3333333333333335
print(21.9 / 9.3)       # 2.354838709677419

# Modulus
print(21 % 9)           # 3
print(-21 % 9)          # 6
print(21.9 % 9.3)       # 3.299999999999997     ~ 3.3

# Exponent
print(2 ** 2)           # 4
print(-2 ** 2)          # -4
print(2.2 ** 2)         # 4.840000000000001     ~ 4.8

# Floor Division
print(100 // 20)        # 5
print(110 // 20)        # 5
print(120 // 20)        # 6