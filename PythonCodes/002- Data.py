# All data are objects
# type() -> function that return the type of the data

print(type(1000))           # int   -> integer number

print(type(10.01))          # float -> floating point number

print(type("Anas"))         # str   -> string

print(type(2 == 2))         # bool  -> boolean

print(type([1, 2]))         # list  -> list

print(type((1, 2)))         # tuple -> tuple

print(type({"One" : 1}))    # dict  -> dictionary