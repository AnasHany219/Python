# Escape Sequance Characters

# [\b] -> back space
print("Hello,,\b World")

# [\] -> Escape New Line + \
print("Hello, \
World")

# [\\] -> Escape Back Slash
print("Hello, World \\")

# [\'] -> Escape Single Quotes
print("Hello \'World\'")

# [\"] -> Escape Double Quotes
print("Hello \"World\"")

# [\n] -> Line Feed
print("First Line \nSecond Line")

# [\r] -> Carriage Return
print("654321 \rAnas")

# [\t] -> Horizontal tab
print("Hello \t World")

# [\xhh] -> Character Hex Value
print("\x41\x6E\x61\x73 \x48\x61\x6E\x79")